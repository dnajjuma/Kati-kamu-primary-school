    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
          DefaultTableModel model = (DefaultTableModel) jTableStudentName.getModel();
        if(jTableStudentName.getSelectedRow()==-1){
            if(jTableStudentName.getRowCount()==0){
                JOptionPane.showMessageDialog(null, "No Student has been Selected",
                        "Alert!", JOptionPane.OK_OPTION);
            }else
                JOptionPane.showMessageDialog(null, "Select the student to be Deleted",
                        "Student Records System", JOptionPane.OK_OPTION);
            
        }
        else{        
            String id = jTableStudentName.getValueAt(jTableStudentName.getSelectedRow(), 0).toString();
            try{
            ps = MyConnection.getConnection().prepareStatement("DELETE FROM student_reg WHERE(student_id ="+'"'+id+'"'+")");
            ps.executeUpdate();
            }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Can Not Delete At this Moment! Try Later"+e.getMessage());
            }
            model.removeRow(jTableStudentName.getSelectedRow());
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

