    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        try {
            String registerQuery ="SELECT * From student_reg";
            DefaultTableModel modele = (DefaultTableModel) jTableStudentName.getModel();        
            ps = MyConnection.getConnection().prepareStatement(registerQuery);
            rs = ps.executeQuery();
            while(rs.next()){
            Object o[]= {rs.getString("student_id"), rs.getString("firstName"), rs.getString("lastName")
                    ,rs.getString("Gender"), rs.getString("Course"), rs.getString("Fee")
                    ,rs.getString("BirthDay"), rs.getString("Phone"), rs.getString("Address")};
            if(jTableStudentName.getRowCount()!= getRowNumber())    
            modele.addRow(o);    
            }
            }catch(SQLException ex){
              Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_formWindowOpened

