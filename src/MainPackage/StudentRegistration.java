    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed

        if(jTableStudentName.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(null,"First Select A Row To Be Edited");
        }
        else if(jTableStudentName.getSelectedRow()!=-1){
            if(jTextId.getText().equals("")||jTextName.getText().equals("")
                    ||jTextLName.getText().equals("")||jTextFees.getText().equals("")
                    ||jTextBirth.getText().equals("")||jTextPhone.getText().equals("")
                    ||jTextAreaAddress.getText().equals("")||jComboBoxCourse.getSelectedItem().equals(""))
                JOptionPane.showMessageDialog(null,"The data is Already Saved!");
            else{
            String combo = jComboBoxCourse.getSelectedItem().toString();
        String id = jTableStudentName.getValueAt(jTableStudentName.getSelectedRow(), 0).toString();
        String Query = "UPDATE student_reg SET student_id = '"+jTextId.getText()+"',"
                + " FirstName='"+jTextName.getText()+"',"
                + " LastName='"+jTextLName.getText()+"',"
                + " Gender='"+RadioButton()+"',"
                + " Course='"+combo+"',"
                + " Fee='"+jTextFees.getText()+"',"
                + " BirthDay='"+jTextBirth.getText()+"',"
                + " Phone='"+jTextPhone.getText()+"',"
                + " Address='"+jTextAreaAddress.getText()+"'"
                + " WHERE student_id = "+'"'+id+'"';
        
        if(!AlreadyExist(jTextId.getText())){
        try{
            ps = MyConnection.getConnection().prepareStatement(Query);
            ps.executeUpdate();
            DeletePreEditedRowFromTable();
            RegisterTable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Can not Edit at this Time Please Try Later!"+e.getMessage()); 

        }
        }
        }
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

    public void DeletePreEditedRowFromTable (){
        DefaultTableModel model = (DefaultTableModel) jTableStudentName.getModel();
        model.removeRow(jTableStudentName.getSelectedRow());
    }
        
    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed

        if(jTextId.getText().trim().equals("")
               ||jTextName.getText().trim().equals("")
               ||jTextLName.getText().trim().equals(""))
           JOptionPane.showMessageDialog(null, "Please Enter student Name and Id", "Alert",1);
       else if(jTextBirth.getText().isEmpty()||jTextPhone.getText().isEmpty()
               ||jTextAreaAddress.getText().isEmpty()||jTextFees.getText().isEmpty())
           JOptionPane.showMessageDialog(null, "Please Enter the Details","Alert",1);
        
       else{
        String ID = jTextId.getText();
        if(!AlreadyExist(ID)){
        String query = "insert into student_reg(student_id, firstName, lastName, Gender, Course, Fee, birthDay, Phone, Address) values(?,?,?,?,?,?,?,?,?)";
        
        try{
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, ID);
            ps.setString(2, jTextName.getText());
            ps.setString(3, jTextLName.getText());
            ps.setString(4, RadioButton());
            ps.setString(5, jComboBoxCourse.getSelectedItem().toString());
            ps.setString(6, jTextFees.getText());
            ps.setString(7, jTextBirth.getText());
            ps.setString(8, jTextPhone.getText());
            ps.setString(9, jTextAreaAddress.getText());
            ps.executeUpdate();
            RegisterTable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Can not Store tha data at this Time Please Try Later!"+e.getMessage()); 

        }        
        }
    }
        
        
    }//GEN-LAST:event_jButtonRegisterActionPerformed
