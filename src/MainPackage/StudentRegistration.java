    private Object RegisterTable(){
        DefaultTableModel model = (DefaultTableModel) jTableStudentName.getModel();
       model.addRow(new Object[]{
           jTextId.getText(),
           jTextName.getText(),
           jTextLName.getText(),
           RadioButton(),
           jComboBoxCourse.getSelectedItem(),
           jTextFees.getText(),
           jTextBirth.getText(),
           jTextPhone.getText(),
           jTextAreaAddress.getText()           
       });
       return 0;
    }
